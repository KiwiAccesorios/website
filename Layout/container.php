        document.getElementById('enviar-btn').addEventListener('click', function() {
            var nombre = document.getElementById('nombre').value.trim();
            var email = document.getElementById('email').value.trim();
            var mensaje = document.getElementById('mensaje').value.trim();
            
            if (nombre && email && mensaje) {
                document.getElementById('mensaje-notificacion').innerHTML = 'Enviado correctamente';
                document.getElementById('mensaje-notificacion').classList.add('success');
                document.getElementById('mensaje-notificacion').classList.remove('error');
            } else {
                document.getElementById('mensaje-notificacion').innerHTML = 'Por favor, completa todos los campos.';
                document.getElementById('mensaje-notificacion').classList.add('error');
                document.getElementById('mensaje-notificacion').classList.remove('success');
            }
        });
    </script>

    <script>
        var modal = document.getElementById("myModal");
        var modal2 = document.getElementById("myModal2");

        var btn = document.querySelectorAll('.boton-saber-mas');
        var btn2 = document.querySelectorAll('.boton-saber-mas1');

        var span = document.getElementsByClassName("close")[0];
        var span2 = document.getElementsByClassName("close2")[0];

        btn.forEach(function(button) {
            button.onclick = function() {
                modal.style.display = "block";
            };
        });

        btn2.forEach(function(button) {
            button.onclick = function() {
                modal2.style.display = "block";
            };
        });

        span.onclick = function() {
            modal.style.display = "none";
        } 
        span2.onclick = function() {
            modal2.style.display = "none";
        }
        window.onclick = function(event) {
            if (event.target === modal) {
                modal.style.display = "none";
            }
            if (event.target === modal2) {
                modal2.style.display = "none";
            }
        }
    </script>